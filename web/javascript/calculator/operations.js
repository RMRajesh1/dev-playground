
/* Operations class */

class Operations {
    constructor() {
        // yet
    }

    addition(x, y) {
        return x + y;
    }

    subtraction(x, y) {
        return x - y;
    }

    multiplication(x, y) {
        return x * y;
    }

    division(x, y) {
        return x / y;
    }

    remainders(x, y) {
        return x % y;
    }

}   // Operations class ends